package com.mrizkisaputra.submission;

public class Product {
    private String productTitleName, productSubtitleName, productPrice, productDetail, productOperatingSystem, productResolusi, productSpek;
    private int productPhoto, productStok;

    public String getProductTitleName() {
        return productTitleName;
    }

    public void setProductTitleName(String productTitleName) {
        this.productTitleName = productTitleName;
    }

    public String getProductSubtitleName() {
        return productSubtitleName;
    }

    public String getProductOperatingSystem() {
        return productOperatingSystem;
    }

    public void setProductOperatingSystem(String productOperatingSystem) {
        this.productOperatingSystem = productOperatingSystem;
    }

    public String getProductResolusi() {
        return productResolusi;
    }

    public void setProductResolusi(String productResolusi) {
        this.productResolusi = productResolusi;
    }

    public String getProductSpek() {
        return productSpek;
    }

    public void setProductSpek(String productSpek) {
        this.productSpek = productSpek;
    }

    public int getProductStok() {
        return productStok;
    }

    public void setProductStok(int productStok) {
        this.productStok = productStok;
    }

    public void setProductSubtitleName(String productSubtitleName) {
        this.productSubtitleName = productSubtitleName;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductDetail() {
        return productDetail;
    }

    public void setProductDetail(String productDetail) {
        this.productDetail = productDetail;
    }

    public int getProductPhoto() {
        return productPhoto;
    }

    public void setProductPhoto(int productPhoto) {
        this.productPhoto = productPhoto;
    }
}
